# Write a program that prints the numbers from 1 to 100.


i = 1 # Initializing i to 1

# The while loop will continue to execute as long as the condition is true
while(i <= 100):
    print(i)
    i += 1 # i = i + 1

for i in range(1, 101):
    print(i)
# The for loop is a more concise way to achieve the same result as the while loop.
# The range() function generates a sequence of numbers from 1 to 100 (inclusive).